package br.com.trabvaguetti;

public class PF extends Pessoa {
	private int id;
	private String cpf;
	private String RG;
	private String ERG;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getRG() {
		return RG;
	}
	public void setRG(String rG) {
		RG = rG;
	}
	public String getERG() {
		return ERG;
	}
	public void setERG(String eRG) {
		ERG = eRG;
	}
	
	
	
}
